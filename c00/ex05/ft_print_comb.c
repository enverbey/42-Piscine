#include <unistd.h>

void	ft_print_comb(void)
{
	int	x;
	int	y;
	int	z;

	x = 1;
	while (x <= 7)
	{
		y = x + 1;
		while (y <= 8)
		{
			z = y + 1;
			while (z <= 9)
			{
				write(1, &("0123456789")[x % 10], 1);
				write(1, &("0123456789")[y % 10], 1);
				write(1, &("0123456789")[z % 10], 1);
				if (x != 7)
					write(1, ", ", 2);
				z++;
			}
			y++;
		}
		x++;
	}
}
